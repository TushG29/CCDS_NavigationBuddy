package com.example.scsebuddy.requestsresults;

public class Course {
    private String code;
    private String title;
    private String aus;
    private int favorite;
    private int year;

    public String getCode() {
        return code;
    }

    public String getTitle() {
        return title;
    }

    public String getAUs() {
        return aus;
    }

    public int getFavorite() {
        return favorite;
    }

    public int getYear() {
        return year;
    }
}
