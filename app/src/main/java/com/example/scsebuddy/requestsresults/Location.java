package com.example.scsebuddy.requestsresults;

public class Location {
    private String name;
    private String code;
    private String photoId;
    private String level;
    private String x;
    private String y;

    public String getName() {
        return name;
    }

    public String getCode() {
        return code;
    }

    public String getPhotoId() {
        return photoId;
    }

    public String getLevel() {
        return level;
    }

    public String getX() {
        return x;
    }

    public String getY() {
        return y;
    }
}
